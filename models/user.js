const mongoose = require('mongoose');

//AdminSchema
const adminSchema = mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
    role: {
        type: String,
        default: "Administrator"
    }

});

module.exports = mongoose.model('Admin', adminSchema);